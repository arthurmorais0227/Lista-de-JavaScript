let nota = 10;

switch (nota) {
  case nota = 10:
  case nota = 9:
    console.log("A");
    break;
  case nota = 8:
  case nota = 7:
    console.log("B");
    break;
  case nota = 6:
  case nota = 5:
    console.log("C");
    break;
  case nota = 4:
  case nota = 3:
    console.log("D");
    break;
  case nota = 2:
  case nota = 1:
  case nota = 0:
    console.log("E");
    break;
  default:
    console.log("Nota inválida! Insira um número de 0 a 10.");
}