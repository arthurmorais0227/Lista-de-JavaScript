let estação = 5;

switch (estação) {
  case estação = 1:
    console.log("Verão! ☀️");
    break;
  case estação = 2:
    console.log("Outono! 🍁");
    break;
  case estação = 3:
    console.log("inverno! ❄️");
    break;
  case estação = 4:
    console.log("Primavera! 🌼");
    break;
  default:
    console.log("Número inserido inválido! Insira um número de 1 a 4.");
}