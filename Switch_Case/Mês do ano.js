let mês = 12;

switch (mês) {
  case mês = 1:
    console.log("Janeiro!");
    break;
  case mês = 2:
    console.log("Fevereiro!");
    break;
  case mês = 3:
    console.log("Março!");
    break;
  case mês = 4:
    console.log("Abril!");
    break;
  case mês = 5:
    console.log("Maio!");
    break;
  case mês = 6:
    console.log("Junho!");
    break;
  case mês = 7:
    console.log("Julho!");
    break;
  case mês = 8:
    console.log("Agosto!");
    break;
  case mês = 9:
    console.log("Setembro!");
    break;
  case mês = 10:
    console.log("Outubro!");
    break;
  case mês = 11:
    console.log("Novembro!");
    break;
  case mês = 12:
    console.log("Dezembro!");
    break;
  default:
    console.log("Número inválido! Insira um número de 1 a 12.");
}
