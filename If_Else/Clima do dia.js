let temperatura = 30;

if (temperatura < 15) {
  console.log(`A temperatura é ${temperatura}°C e está frio ❄️.`);
}else if (temperatura >= 15 && temperatura <= 25) {
  console.log(`A temperatura é ${temperatura}°C e está agradável 🙂.`);
}else {
  console.log(`A temperatura é ${temperatura}°C e está quente 🔥.`);
}
